#include <stdio.h>
#include <stdlib.h>
#include "arithmatic.h"

int add(int a, int b)
{
    int nAdd = a + b;
    return nAdd;
}

int sub(int a, int b)
{
    int nSub = a - b;
    return nSub;
}

int mul(int a, int b)
{
    int nMul = a * b;
    return nMul;
}

int mod(int a, int b)
{
    int nMod = a % b;
    return nMod;
}
